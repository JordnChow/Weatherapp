  function convertUnixToDay(d){
    const date= new Date(d * 1000)
    return date.getUTCDate() 
}

function display5dayForcast(weather) {
    const dates = [];
    const prev = [];
    weather.forEach(item => {
        const dt1 = convertUnixToDay(item.date_unix)
        if(dates.includes(dt1)){
            return;
        } else {
            dates.push(dt1)
            prev.push(item)
        }
    })
    prev.shift()
    return prev
}

